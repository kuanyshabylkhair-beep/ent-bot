# АДМИН КОМАНДЫ
# ══════════════════════════════════════════════
async def activate_cmd(update: Update, context: ContextTypes.DEFAULT_TYPE):
    if update.effective_user.id != ADMIN_ID:
        await update.message.reply_text(f"Нет доступа. Твой ID: {update.effective_user.id}")
        return
    args = context.args
    if len(args) < 2:
        await update.message.reply_text("Использование: /activate [user_id] [standard|premium|premium_test]")
        return
    target_uid, plan = args[0], args[1]
    if plan not in ("standard", "premium", "premium_test"):
        await update.message.reply_text("План: standard, premium или premium_test")
        return
    users = load_users()
    user = get_user(users, target_uid)
    user["plan"] = plan
    user["plan_until"] = (datetime.now() + timedelta(days=30)).isoformat()
    user["pending_payment"] = None
    save_users(users)
    plan_name = PLAN_NAMES[plan]
    exp_date = (datetime.now() + timedelta(days=30)).strftime("%d.%m.%Y")

    extra = "\n\n🧪 Тебе доступен Режим теста — до 50 вопросов в день!" if plan == "premium_test" else ""
    notice = (
        "🎉 *Подписка активирована!*\n\n"
        f"✅ Тариф: *{plan_name}*\n"
        f"📅 Действует до: *{exp_date}*"
        f"{extra}\n\n"
        "Удачи на ЕНТ! 🚀"
    )
    try:
        await context.bot.send_message(
            chat_id=int(target_uid),
            text=notice,
            parse_mode="Markdown",
            reply_markup=main_menu()
        )
        await update.message.reply_text(f"✅ Активировано и отправлено: {plan_name} для {target_uid} до {exp_date}")
    except Exception as e:
        await update.message.reply_text(f"✅ Активировано: {plan_name} до {exp_date}\n❌ Сообщение не дошло: {e}")

async def users_cmd(update: Update, context: ContextTypes.DEFAULT_TYPE):
    if update.effective_user.id != ADMIN_ID:
        return
    users = load_users()
    premium_count = sum(1 for u in users.values() if is_premium(u))
    lines = [f"👥 *Всего: {len(users)} | ⭐️ Премиум: {premium_count} | 🆓 Бесплатных: {len(users)-premium_count}*\n"]
    for uid, u in users.items():
        if is_premium(u):
            until = datetime.fromisoformat(u["plan_until"]).strftime("%d.%m")
            plan_label = PLAN_NAMES.get(u.get("plan"), u.get("plan"))
            lines.append(f"⭐️ {u.get('name','?')} `{uid}` — {plan_label} до {until}")
        else:
            lines.append(f"🆓 {u.get('name','?')} `{uid}`")
    await update.message.reply_text("\n".join(lines), parse_mode="Markdown")

async def refresh_menu_cmd(update: Update, context: ContextTypes.DEFAULT_TYPE):
    if update.effective_user.id != ADMIN_ID:
        return
    users = load_users()
    sent, failed = 0, 0
    await update.message.reply_text(f"🔄 Обновляю меню у {len(users)} пользователей...")
    for uid in users.keys():
        try:
            await context.bot.send_message(
                chat_id=int(uid),
                text="🔄 Меню обновлено! Появилась новая кнопка *🧪 Режим теста* 👇",
                parse_mode="Markdown",
                reply_markup=main_menu()
            )
            sent += 1
            await asyncio.sleep(0.3)
        except Exception as e:
            failed += 1
            logger.warning(f"Не удалось обновить меню {uid}: {e}")
    await update.message.reply_text(f"✅ Готово! Отправлено: {sent}, ошибок: {failed}")


