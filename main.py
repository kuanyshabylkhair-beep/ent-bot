# CALLBACK КНОПКИ
# ══════════════════════════════════════════════
async def button_callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    data = query.data

    if data == "show_premium":
        users = load_users()
        user = get_user(users, query.from_user.id)
        await show_premium_menu(query.message.reply_text, user)
        return

    if data.startswith("test_subject|"):
        _, subject = data.split("|", 1)
        users = load_users()
        user = get_user(users, query.from_user.id)
        if not has_test_access(user):
            await query.message.reply_text("🧪 Режим теста доступен только в тарифе Премиум + Тест.")
            return
        user["test_subject"] = subject
        user["test_mode"] = True
        save_users(users)
        label = "Все предметы" if subject == "all" else subject
        await query.message.reply_text(f"🧪 Начинаем тест по теме: *{label}*", parse_mode="Markdown")
        await send_test_question(context.bot, query.from_user.id)
        return

    if data in ("buy_standard", "buy_premium", "buy_premium_test"):
        plan = data.replace("buy_", "")
        price = PLAN_PRICES[plan]
        plan_name = PLAN_NAMES[plan]

        users = load_users()
        user = get_user(users, query.from_user.id)
        user["pending_payment"] = plan
        save_users(users)

        text = (
            f"💳 *Оплата тарифа «{plan_name}»*\n\n"
            f"Сумма: *{price} ₸*\n\n"
            f"📱 Переведи на Kaspi:\n"
            f"`{KASPI_NUMBER}`\n\n"
            f"В комментарии напиши свой *Telegram ID*:\n"
            f"`{query.from_user.id}`\n\n"
            f"После перевода нажми кнопку ниже 👇"
        )
        keyboard = [[InlineKeyboardButton("✅ Я оплатил!", callback_data=f"paid|{plan}|{price}")]]
        await query.message.reply_text(text, parse_mode="Markdown", reply_markup=InlineKeyboardMarkup(keyboard))
        return

    if data.startswith("paid|"):
        _, plan, price = data.split("|")
        uid = query.from_user.id
        raw_name = query.from_user.first_name or "Пользователь"
        # Убираем символы, которые ломают Markdown-разметку (* _ ` [ ])
        safe_name = re.sub(r'[*_`\[\]]', '', raw_name)
        username = f"@{query.from_user.username}" if query.from_user.username else "без юзернейма"
        plan_name = PLAN_NAMES.get(plan, plan)

        admin_text = (
            f"💰 *Новая оплата!*\n\n"
            f"👤 {safe_name} ({username})\n"
            f"🆔 ID: `{uid}`\n"
            f"📦 Тариф: {plan_name}\n"
            f"💵 Сумма: {price} ₸\n\n"
            f"Подтверди оплату на Kaspi и нажми кнопку 👇"
        )
        admin_keyboard = [[
            InlineKeyboardButton(f"✅ Активировать {plan_name}", callback_data=f"adm_act|{uid}|{plan}")
        ]]
        try:
            await context.bot.send_message(
                chat_id=ADMIN_ID, text=admin_text, parse_mode="Markdown",
                reply_markup=InlineKeyboardMarkup(admin_keyboard)
            )
        except Exception as e:
            logger.error(f"Не удалось уведомить админа: {e}")
            # Запасной вариант — без Markdown вообще, чтобы админ точно получил сообщение
            try:
                plain_text = f"Новая оплата!\n\n{safe_name} ({username})\nID: {uid}\nТариф: {plan_name}\nСумма: {price} тг\n\nАктивируй: /activate {uid} {plan}"
                await context.bot.send_message(chat_id=ADMIN_ID, text=plain_text, reply_markup=InlineKeyboardMarkup(admin_keyboard))
            except Exception as e2:
                logger.error(f"Запасной вариант тоже не сработал: {e2}")

            await query.message.reply_text(f"DEBUG ADMIN ERROR: {e}")

        support_keyboard = [[InlineKeyboardButton("💬 Написать в поддержку", url=f"https://t.me/{SUPPORT_USERNAME}")]]
        await query.message.reply_text(
            "✅ *Заявка принята!*\n\n"
            "Проверяем оплату и активируем подписку в течение *5–15 минут*.\n"
            "Если прошло больше 30 минут — напиши нам 👇",
            parse_mode="Markdown",
            reply_markup=InlineKeyboardMarkup(support_keyboard)
        )
        return

    if data.startswith("adm_act|"):
        if query.from_user.id != ADMIN_ID:
            return
        _, target_uid, plan = data.split("|")
        users = load_users()
        user = get_user(users, target_uid)
        user["plan"] = plan
        user["plan_until"] = (datetime.now() + timedelta(days=30)).isoformat()
        user["pending_payment"] = None
        save_users(users)
        plan_name = PLAN_NAMES[plan]
        exp_date = (datetime.now() + timedelta(days=30)).strftime("%d.%m.%Y")

        extra = "\n\n🧪 Тебе доступен Режим теста — до 50 вопросов в день!" if plan == "premium_test" else ""
        notice = (
            "🎉 *Подписка активирована!*\n\n"
            f"✅ Тариф: *{plan_name}*\n"
            f"📅 Действует до: *{exp_date}*"
            f"{extra}\n\n"
            "Удачи на ЕНТ! 🚀"
        )
        try:
            await context.bot.send_message(
                chat_id=int(target_uid), text=notice, parse_mode="Markdown", reply_markup=main_menu()
            )
            await query.edit_message_text(f"✅ Активировано: {plan_name} для {target_uid} до {exp_date}\n(клиент уведомлён)")
        except Exception as e:
            await query.edit_message_text(f"✅ Активировано: {plan_name} до {exp_date}\n❌ Клиент не уведомлён: {e}")
        return

    if data.startswith("ans|"):
        parts = data.split("|")
        if len(parts) < 6:
            return
        _, chat_id, subject, idx, chosen, mode = parts
        idx = int(idx); chat_id = int(chat_id)
        q = QUESTIONS[subject][idx]
        is_correct = chosen == q["ans"]
        users = load_users()
        user = get_user(users, chat_id)
        st = user["stats"].setdefault(subject, {"correct": 0, "total": 0})
        st["total"] += 1
        if is_correct:
            st["correct"] += 1
        save_users(users)
        result = f"✅ *Правильно!* Молодец, {user['name']}!\n\n" if is_correct else f"❌ *Неверно.* Ты: {chosen}, правильно: *{q['ans']}*\n\n"
        result += f"💡 {q['exp']}\n\n📊 {subject}: {st['correct']}/{st['total']}"
        await query.edit_message_reply_markup(reply_markup=None)
        await context.bot.send_message(chat_id=chat_id, text=result, parse_mode="Markdown")

        # Если это режим теста — сразу шлём следующий вопрос
        if mode == "test":
            users = load_users()
            user = get_user(users, chat_id)
            if has_test_access(user) and can_get_test_question(user):
                await send_test_question(context.bot, chat_id)
            elif has_test_access(user):
                await context.bot.send_message(
                    chat_id=chat_id,
                    text=f"🏁 Лимит теста на сегодня исчерпан ({TEST_DAILY_LIMIT}/{TEST_DAILY_LIMIT}). Отличная тренировка! Заходи завтра 💪"
                )

