    """Превращает отображаемое название предмета в канонический (русский) ключ для поиска в QUESTIONS_RU."""
    if lang == "kk" and subject in SUBJECT_MAP_KK_RU:
        return SUBJECT_MAP_KK_RU[subject]
    return subject

