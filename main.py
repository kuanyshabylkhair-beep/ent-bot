async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    uid = update.effective_user.id
    name = update.effective_user.first_name or "Ученик"
    users = load_users()
    user = get_user(users, uid)
    user["name"] = name
    save_users(users)

    if is_premium(user):
        plan_text = f"⭐️ У тебя активен *{PLAN_NAMES[user['plan']]}*!"
    else:
        plan_text = "🆓 Сейчас у тебя *бесплатный план* (1 вопрос/день, История КЗ)"

    await update.message.reply_text(
        f"👋 Привет, *{name}*!\n\n"
        f"🎯 Я твой репетитор для *ЕНТ 2026-2027*\n\n"
        f"{plan_text}\n\n"
        f"Используй кнопки меню внизу 👇",
        parse_mode="Markdown",
        reply_markup=main_menu()
    )
    await send_question(context.bot, uid)

